/**
 * @page cmds Commands  
 * 
 * Drush_multi provides the following commands:
 * 
 * @subpage multi_status_help "multi-status"
 *  
 * An extended drush core status
 *  
 * @subpage multi_create_help "multi-create"
 * 
 * Creates a Drupal multisite installation, 
 * supports usage of <a href="http://drupal.org/project/drush_make">drush_make</a> makefiles
 *  
 * @subpage multi_exec_help "multi-exec"
 * 
 * Execute drush core commands on all sites (batch mode)
 *  
 * @subpage multi_drupalupdate_help "multi-drupalupdate"
 * 
 * Updates the installation if there is a new minor release available
 *  
 * @subpage multi_sql_dump_help "multi-sql-dump"
 * 
 * Execute a sql dump on all sites (batch mode)
 *  
 * @subpage multi_nagios_help "multi-nagios"
 * 
 * Command for use as nagios plugin with exit status and status message
 *  
 * @subpage multi_site_help "multi-site"
 * 
 * Creates a site within the installation
